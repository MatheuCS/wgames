      <h2>Detonado Diablo 3:<br />
    </h2>
      <p>Diablo 3 apresenta quatro níveis de dificuldade que será progressivamente desafiam jogadores</p>
      <p>Enquanto o reinício de aventura desde o início, os inimigos tornam-se mais difícil de derrotar e têm o   potencial para largar melhores recompensas (loot!). Novas armaduras,   armas, e as receitas têm uma chance de aparecer, que teria sido   indisponível antes, por isso os jogadores terão de enfrentar as mais   difíceis configurações de dificuldade para ter uma chance de equipar   seus personagens com itens ainda melhores.</p>
      <p><strong>Dificuldade níveis recomendados</strong><br />
        1-30 normais<br />
        Pesadelo 31-50<br />
        Inferno 50 (obrigatória) para 60<br />
        Inferno 60 (obrigatório)<br />
  <br />
        Deve   notar-se que o nível só tem importância no caso de desbloqueio   habilidades uma personagem (que melhoram à medida que aumenta de nível).   Equipamento de um personagem é tão importante, uma vez que a armadura e   armas são os itens que fora a base de competências seus danos e os valores de proteção. Então ... se acontecer de você ter   uma arma que faz 2,1 mil milhões de pontos de dano, você tem a certeza   que você vai matar tudo (incluindo o último chefe Act IV, no Inferno,   com quatro jogadores jogando) em uma batida. Por outro lado, se você tem   uma arma que só faz 10,0 pontos de dano no nível 60, você pode ter um   problema. Desbloquear dificuldades mais difíceis Completando todos os   atos para um dado nível de dificuldade abre a próxima dificuldade   progressiva. Por exemplo, batendo normal desbloqueia Nightmare, que deve   ser derrotado para acessar dificuldade Hell. Embora não existem   restrições de nível de per si, cada dificuldade tem sido ajustado para   ser jogado dentro de uma gama nível específico.</p>
