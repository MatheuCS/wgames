        <h2>Analise:</h2>
        <p>Em um momento em que os lançamentos para a geração atual de consoles são constituídos em sua maioria por sequências de títulos consagrados, Dishonored surge como uma verdadeira rajada de ar fresco. Apresentando um universo completamente inédito, o game tem como cenário principal a cidade fictícia de Dunwall, um local em que as ruas infectadas pela praga são tão podres quanto os políticos responsáveis por sua administração.</p>
        <p>Você assume o papel de Corvo Attano, ex-protetor do reino que é acusado de traição após ter falhado em defender a imperatriz de um ataque perpetuado por traidores. Ajudado por rebeldes e uma entidade misteriosa, o herói parte em busca de vingança, perpetuando uma série de assassinatos que tentam reestabelecer a antiga dinastia.</p>
        <p>Apresentando uma mistura de ação, furtividade e até mesmo estratégia, o game chegou aos PCs e consoles no dia 9 de outubro deste ano.</p>
        <h3>Pontos aprovados: </h3>
        <p><strong>Liberdade</strong></p>
        <p>Ao contrário de outros títulos com a designação AAA lançados recentemente, Dishonored é uma experiência marcada pela liberdade de escolha. Fora a área inicial do título, todos os demais mapas apresentam diversos caminhos alternativos responsáveis por propiciar diferentes maneiras de lidar com o objetivo proposto ao protagonista.</p>
        <p><strong>Sistema de caos</strong></p>
        <p>Assim como em games como Deus EX: Human Revolution (do qual Dishonored tira muitas influências), você pode optar por agir ou não de maneira letal durante os combates. Caso opte pelo caminho passivo, é possível enforcar inimigos e deixá-los inconscientes ou usar dardos paralisantes para tirá-los do caminho.</p>
        <p><strong>Estilo de arte impressionante</strong></p>
        <p>Assim que você entra no universo de Dishonored, a primeira impressão é a de que fomos lançados em uma versão alternativa do mundo de BioShock. Felizmente, apesar da similaridade entre os dois títulos, o game da Arkane Studios consegue ter personalidade própria, apresentando um mundo decadente em que robôs gigantes convivem pacificamente com carabinas antigas.</p>
        <p><strong>Duração apropriada</strong></p>
        <p>Jogadores dedicados que decidirem partir com tudo para cima dos inimigos poderão terminar Dishonored em questão de seis horas. Porém, caso você prefira explorar atentamente cada cenário e atuar de forma mais furtiva, esse valor pode dobrar ou até mesmo triplicar.</p>
        <h3>Pontos Reprovados:</h3>
        <p><strong>Pequenos tropeços gráficos</strong></p>
        <p>Quem está acostumado com títulos como Battlefield 3, Call of Duty: Black Ops 2 pode achar que Dishonored é um jogo com um visual antigo para o gênero de primeira pessoa. Isso se deve ao fato de que, apesar de ele ter uma direção de arte primorosa, fica evidente que a Unreal Engine 3 já não é mais a opção mais poderosa da indústria.</p>
        <p><strong>História com pontas soltas</strong></p>
        <p>Embora em geral a história de Dishonored seja bem conduzida e renda um final satisfatório, não é possível deixar de notar algumas pontas soltas nela. Exemplo disso é a figura misteriosa que dá os poderes a Corvo, cujas origens e propósitos nunca ficam realmente claros. Em um universo tão rico quanto o criado pela Arkane Studios, esses conflitos não resolvidos acabam soando como mera falta de cuidado dos desenvolvedores.</p>
        <p><strong>Controles complicados no PC</strong></p>
        <p>Assim como outros games desenvolvidos para a geração atual de consoles, Dishonored sofre alguns problemas em sua transição para o PC. Apesar de não ser exatamente impossível de ser dominada, a configuração-padrão de teclas adotada pelo título não é exatamente intuitiva, fazendo com que você se perca muitas vezes para realizar alguma ação.</p>
        <h3 id="conclusion">Vale a pena?</h3>
        <p>Entre os diversos lançamentos grandiosos que devem surgir durante o período final de 2012, Dishonored se destaca por não apelar para velhas fórmulas ou para personagens famosos como forma de chamar a atenção. Entre os aspectos que se destacam no game estão seu estilo de arte que mistura elementos antigos e futuristas, sua história intrigante e a opção por um estilo de jogabilidade que funciona muito bem.</p>
        <p>Poucas sensações são melhores do que, após explorar cuidadosamente um ambiente, detectar o ponto ideal para sufocar ou matar um inimigo. Felizmente, o protagonista conta com poderes interessantes o bastante para propiciar diferentes táticas de jogo, seja você alguém que prefere adotar estratégias mais silenciosas ou partir para o combate direto.</p>
        <p>Claro, o game não é exatamente perfeito, como bem comprovam os gráficos um tanto quanto antiquados, que em muitos momentos apresentam modelos de personagens com animações simplificadas e texturas em baixa resolução. Porém, isso não chega a ser exatamente um problema, já que esses defeitos podem ser ignorados facilmente.</p>
        <p>Todas as qualidades apresentadas por Dishonored fazem com que ele seja um jogo bastante memorável e que com certeza vai ser lembrado nas eleições para o melhor lançamento de 2012. Depois dele, o Arkane Studios finalmente deve ter o lugar que merece entre os nomes que se destacam na indústria.</p>
