<h2>Análise:</h2>
<div>
  <div>
    <p>Lara Croft foi, por muito tempo, a maior representante feminina do mundo dos videogames. Uma musa inspiradora para jogadores, produtores e desenvolvedores de jogos. Porém, isso mudou com o passar do tempo e bustos fartos e acrobacias fantásticas deixaram de ser fatores muito atraentes para novos jogadores.<br />
      <br />
      A dama virou apenas uma sombra do que era. Com este novo game, a Crystal Dynamics fez ela renascer com esplendor. Agora temos uma personagem consistente, rica em emoções e de personalidade forte e marcante. Lara Croft voltou das cinzas como uma fênix, mostrando que não esqueceu como estrelar um jogo de aventura.<br />
      <br />
      &ldquo;Tomb Raider&rdquo; é um game que mostra o sofrimento e crescimento de uma heroína de verdade. Você se apega a Lara de uma forma que não imaginava ser possível. fazendo com que os jogadores se sintam no controle da moça até mesmo durante as cenas de corte.<br />
      <br />
